class Solution06 {
    public void setMatrixZeroes(int[][] mat) {
        int n = mat.length;        // Number of rows
        int m = mat[0].length;     // Number of columns
        boolean firstRowZero = false;
        boolean firstColZero = false;

        // Step 1: Determine if the first row or first column should be zero
        for (int i = 0; i < n; i++) {
            if (mat[i][0] == 0) {
                firstColZero = true;
                break;
            }
        }
        for (int j = 0; j < m; j++) {
            if (mat[0][j] == 0) {
                firstRowZero = true;
                break;
            }
        }

        // Step 2: Mark rows and columns to be zeroed
        for (int i = 1; i < n; i++) {
            for (int j = 1; j < m; j++) {
                if (mat[i][j] == 0) {
                    mat[i][0] = 0;  // Mark first cell of the row
                    mat[0][j] = 0;  // Mark first cell of the column
                }
            }
        }

        // Step 3: Zero out cells based on markers
        for (int i = 1; i < n; i++) {
            for (int j = 1; j < m; j++) {
                if (mat[i][0] == 0 || mat[0][j] == 0) {
                    mat[i][j] = 0;
                }
            }
        }

        // Step 4: Handle the first row and first column separately
        if (firstRowZero) {
            for (int j = 0; j < m; j++) {
                mat[0][j] = 0;
            }
        }
        if (firstColZero) {
            for (int i = 0; i < n; i++) {
                mat[i][0] = 0;
            }
        }
    }
}

